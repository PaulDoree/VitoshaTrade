#ifndef TIME_PERIOD_H
#define TIME_PERIOD_H

enum TimePeriod {

	/**
	 * No time period at all.
	 */
	NO = 0,

	/**
	 * One minute.
	 */
	M1 = 1,

	/**
	 * Five minutes.
	 */
	M5 = 5,

	/**
	 * Fifteen minutes.
	 */
	M15 = 15,

	/**
	 * Thirty minutes.
	 */
	M30 = 30,

	/**
	 * One hour.
	 */
	H1 = 60,

	/**
	 * Four hours.
	 */
	H4 = 240,

	/**
	 * One day.
	 */
	D1 = 1440,

	/**
	 * One week.
	 */
	W1 = 10080,

	/**
	 * One month.
	 */
	MN1 = 43200
};

#endif
